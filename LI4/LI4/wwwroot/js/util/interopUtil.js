/**
 * Helper for JSConsole, to turn an array of arguments into rest parameters for JS' console.
 * @param {any} args
 */
function consoleLogHelper(args) {
    console.log(...args);
}

/**
 * Helper for JSConsole, to turn an array of arguments into rest parameters for JS' console.
 * @param {any} args
 */
function consoleErrorHelper(args) {
    console.error(...args);
}